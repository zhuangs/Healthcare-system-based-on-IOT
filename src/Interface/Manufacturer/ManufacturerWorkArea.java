    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel) vaccineJTable.getModel();
        dtm.setRowCount(0);
        for(Vaccine vaccine : ((ManufacturerRole)userAccount.getRole()).getVaccineCatalog().getVaccineList()){
            Object[] row = new Object[4];
            row[0] = vaccine;
            row[1] = vaccine.getName();
            row[2] = vaccine.getDiseaseName();
            row[3] = vaccine.getQuantity();
            dtm.addRow(row);
        }
    }
    
    
    public void populateText(Vaccine vaccine){
        nameJTextField.setText(vaccine.getName());
        priceJTextField2.setText(String.valueOf(vaccine.getPrice()));
        quantityjTextField3.setText(String.valueOf(vaccine.getQuantity()));
        validjTextField4.setText(vaccine.getValidTerm());
        statejTextField5.setText(vaccine.getManufacturerDate());
    }
    
    
    public void populateOrderTable(){
        DefaultTableModel dtm = (DefaultTableModel) orderJTable1.getModel();
        dtm.setRowCount(0);
        for(VaccineRequestItem vaccineRequestItem : manufacturerRequest.getVaccineRequestItemList()){
            Object[] row = new Object[4];
            row[0] = vaccineRequestItem;
            row[1] = vaccineRequestItem.getVaccine().getName();
            row[2] = vaccineRequestItem.getVaccine().getDiseaseName();
            row[3] = vaccineRequestItem.getAmount();
            dtm.addRow(row);
        }
        
    }
    
    public void populateSellHistory(){
        DefaultTableModel dtm = (DefaultTableModel) sellHistoryJTable.getModel();
        dtm.setRowCount(0);
        for(ManufacturerVaccineSaleItem vaccineSaleItem : ((ManufacturerRole)userAccount.getRole()).getManufacturerVaccineSaleItemList()){
            Object[] row = new Object[5];
            row[0] = vaccineSaleItem.getVaccine();
            row[1] = vaccineSaleItem.getVaccine().getName();
            row[2] = vaccineSaleItem.getAmount();
            row[3] = vaccineSaleItem.getVaccineSaleTime();
            row[4] = vaccineSaleItem.getType();
            dtm.addRow(row);
           
            
        }
    }
    
    
    public void populateOrderHistoryTable(){
        DefaultTableModel dtm = (DefaultTableModel) orderhistoryJTable.getModel();
        dtm.setRowCount(0);
        for(WorkRequest workRequest : userAccount.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[3];
            row[0] = workRequest.getWorkRequestID();
            row[1] = workRequest.getStatus() == "Sent" ? "Pending" : workRequest.getStatus();
            row[2] = ((ManufacturerRequest)workRequest).getOption();
            dtm.addRow(row);
        }
        
    }
