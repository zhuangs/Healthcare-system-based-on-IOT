    private void addJButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = vaccineJTable.getSelectedRow();
        if(selectedRow < 0 ){
             JOptionPane.showMessageDialog(null, "Please select a row!");
             return;
        }
        else{
            Vaccine vaccinehere = (Vaccine) vaccineJTable.getValueAt(selectedRow, 0);
            int quantity = (int)quantityjSpinner2.getValue();
            if( quantity < 0 || quantity > vaccinehere.getQuantity()){
                JOptionPane.showMessageDialog(null, "Please change the Quantity you want to add!");
                return;
            }
            
                Boolean isinclude = false;
                for(VaccineRequestItem vaccineRequestItem : manufacturerRequest.getVaccineRequestItemList()){
                    if(vaccineRequestItem.getVaccine().getName().equals(vaccineRequestItem.getVaccine().getName())){
                        int newquantity = vaccinehere.getQuantity() - quantity;
                        int amount = vaccineRequestItem.getAmount();
                        amount = amount + quantity;
                        vaccinehere.setQuantity(newquantity);
                        vaccineRequestItem.setAmount(amount);
                        isinclude = true;
                    }
                }
                if( !isinclude ){
                    VaccineRequestItem vaccineRequestItem = new VaccineRequestItem();
                    vaccineRequestItem.setAmount(quantity);
                    vaccineRequestItem.setVaccine(vaccinehere);
                    int newquantity = vaccinehere.getQuantity() - quantity;
                    vaccinehere.setQuantity(newquantity);
                    manufacturerRequest.getVaccineRequestItemList().add(vaccineRequestItem);
                }
                
            
            populateTable();
            populateOrderTable();
        }
        
    }//GEN-LAST:event_addJButton1ActionPerformed

