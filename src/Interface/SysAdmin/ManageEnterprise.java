    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed
        // TODO add your handling code here:
        Enterprise.EnterpriseType type = (Enterprise.EnterpriseType)enterpriseCombo.getSelectedItem();
        Network networkhere = (Network)networkComboBox.getSelectedItem();
        if(type.getValue().equals(Enterprise.EnterpriseType.CDC.getValue())){
            for(Enterprise enterprisecheck : networkhere.getEnterpriseDirectory().getEnterpriseList()){
                if(enterprisecheck instanceof CDCEnterprise){
                    JOptionPane.showMessageDialog(null, "You have already created a CDC!");
                    return;
                }
            }
        }
        AddEnterprise addenter = new AddEnterprise(userProcessContainer,networkhere,type);
        userProcessContainer.add("AddEnterprise",addenter);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);

    }//GEN-LAST:event_addJButtonActionPerformed

    private void editJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editJButtonActionPerformed
        // TODO add your handling code here
        int seletedRow = enterpriseJTable.getSelectedRow();
        if(seletedRow < 0 ){
            JOptionPane.showMessageDialog(null, "Please select a row!");
        }
        else{
            Enterprise enterprise = (Enterprise)enterpriseJTable.getValueAt(seletedRow, 1);
            EditEnterprise ee = new EditEnterprise(userProcessContainer,enterprise);
            userProcessContainer.add("EditEnterprise", ee);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_editJButtonActionPerformed

    private void deleteJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteJButtonActionPerformed
        // TODO add your handling code here:
        int seletedRow = enterpriseJTable.getSelectedRow();
        if(seletedRow < 0 ){
            JOptionPane.showMessageDialog(null, "Please select a row!");
        }
        else{
            
            int p =  JOptionPane.showConfirmDialog(null, "Do you want to delete this?","Delete",JOptionPane.YES_NO_OPTION);
            if(p == 0){
                    Enterprise enterprise = (Enterprise)enterpriseJTable.getValueAt(seletedRow, 1);
                    enterprise.setStatus("Disable");
            }
        }
    }//GEN-LAST:event_deleteJButtonActionPerformed

