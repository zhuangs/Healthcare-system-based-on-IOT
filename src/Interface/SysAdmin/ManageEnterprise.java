    public void populateNetworkCombo(){
        networkComboBox.removeAllItems();
        for(Network network : business.getNetworkDirectory().getNetworkList()){
            networkComboBox.addItem(network);
        }
        
    }
    public void populateEnterprise(){
        enterpriseCombo.removeAllItems();
        for(Enterprise.EnterpriseType type : Enterprise.EnterpriseType.values()){
            enterpriseCombo.addItem(type);
        }
        
    }

    public void populateEnterpriseTable(Network networkhere){
        DefaultTableModel dtm = (DefaultTableModel) enterpriseJTable.getModel();
        dtm.setRowCount(0);
        for(Enterprise enterprisehere : networkhere.getEnterpriseDirectory().getEnterpriseList()){
            
            Object[] row = new Object[4];
            row[0] = networkhere.getNetworkName();
            row[1] = enterprisehere;
            row[2] = enterprisehere.getName();
            row[3] = enterprisehere.getStatus();
            
            dtm.addRow(row);
            
        }
    }
