    private void deleteJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteJButtonActionPerformed
        // TODO add your handling code here:
        int seletedRow = diseaseJTable.getSelectedRow();
        if(seletedRow < 0 ){
            JOptionPane.showMessageDialog(null, "Please select a row!");
        }
        else{
            Disease disease = (Disease)diseaseJTable.getValueAt(seletedRow, 0);
            business.getDiseaseCatalog().getDiseaseList().remove(disease);
            populateTable();
        }
    }//GEN-LAST:event_deleteJButtonActionPerformed

    private void editJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editJButtonActionPerformed
        // TODO add your handling code here
        int seletedRow = diseaseJTable.getSelectedRow();
        if(seletedRow < 0 ){
            JOptionPane.showMessageDialog(null, "Please select a row!");
        }
        else{
            editJPanel.setVisible(true);
            diseaseIntable = (Disease) diseaseJTable.getValueAt(seletedRow, 0);
            nameeditJTextField1.setText(diseaseIntable.getDiseaseName());
        }
    }//GEN-LAST:event_editJButtonActionPerformed

    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed
        // TODO add your handling code here:
        if(nameJTextField.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Please input a name!");
        }
        else{
            Disease diseasehere = business.getDiseaseCatalog().addDisease();
            diseasehere.setDiseaseName(nameJTextField.getText());
            populateTable();
            
        }
        
    }//GEN-LAST:event_addJButtonActionPerformed

