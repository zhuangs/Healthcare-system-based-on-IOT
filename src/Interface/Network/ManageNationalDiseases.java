    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel) diseaseJTable.getModel();
        dtm.setRowCount(0);
        for(Disease disease : business.getDiseaseCatalog().getDiseaseList()){
            Object[] row = new Object[2];
            row[0] = disease;
            row[1] = disease.getDiseaseName();
            dtm.addRow(row);
        }
    }
    
    
