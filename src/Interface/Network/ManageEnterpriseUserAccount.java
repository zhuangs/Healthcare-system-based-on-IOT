    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel) enterpriseJTable1.getModel();
        dtm.setRowCount(0);
        for(Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()){
            Object[] row = new Object[3];
            row[0] = enterprise;
            row[1] = enterprise.getName();
            row[2] = enterprise.getStatus();
            dtm.addRow(row);
        }
        
    }
     public void populateUserJTable(){
        DefaultTableModel dtm = (DefaultTableModel) userJTable.getModel();
        dtm.setRowCount(0);
        for(Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()){
            for(UserAccount userAccount : enterprise.getUserAccountDirectory().getUserAccountList()){
                    Object[] row = new Object[3];
                    row[0] = enterprise;
                    row[1] = enterprise.getName();
                    row[2] = userAccount;
                    dtm.addRow(row);
            }
        }
    }
