    private void addJButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = enterpriseJTable1.getSelectedRow();
        if(selectedRow < 0 ){
            JOptionPane.showMessageDialog(null, "Please select a row!");
        }
        else{
            Enterprise enterprisehere = (Enterprise) enterpriseJTable1.getValueAt(selectedRow,0);
            if(enterprisehere instanceof Manufacturer){
                try{
                    enterprisehere.getUserAccountDirectory().getUserAccountList().get(0);
                    JOptionPane.showMessageDialog(null, "You already have a Manufacturer User Account!");
                    return;
                }
                catch(Exception e){
                    AddUserAccount addu = new AddUserAccount(userProcessContainer, enterprisehere);
                    userProcessContainer.add("AddUserAccount", addu);
                    CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                    layout.next(userProcessContainer);
                }
                
            }
            else{
                AddUserAccount addu = new AddUserAccount(userProcessContainer, enterprisehere);
                userProcessContainer.add("AddUserAccount", addu);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }
            
        }
    }//GEN-LAST:event_addJButton1ActionPerformed

