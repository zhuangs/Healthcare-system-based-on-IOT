    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        // TODO add your handling code here:
        if(billing.getStatus().equals("Paid")){
            JOptionPane.showMessageDialog(null,"You have already paid!");
            return;
        }
            billing.setStatus("Paid");
            for(InjectionItem injectionItemhere : billing.getInjectionItemList()){
                for(Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()){
                    if(enterprise instanceof Manufacturer){
                        if(enterprise.getName().equals(injectionItemhere.getVaccine().getManufacturerName())){
                            UserAccount userAccounthere = (UserAccount) enterprise.getUserAccountDirectory().getUserAccountList().get(0);
                            ManufacturerVaccineSaleItem mvsi = new ManufacturerVaccineSaleItem();
                            mvsi.setAmount(injectionItemhere.getQuantity());
                            mvsi.setVaccine(injectionItemhere.getVaccine());
                            mvsi.setVaccineSaleTime(mvsi.getTimestampe());
                            mvsi.setType("Clinic Sold");
                            ((ManufacturerRole)userAccounthere.getRole()).getManufacturerVaccineSaleItemList().add(mvsi);
                        }
                    }
                }

            }
            JOptionPane.showMessageDialog(null,"Successfully!");
        
        
        
    }//GEN-LAST:event_submitJButtonActionPerformed

