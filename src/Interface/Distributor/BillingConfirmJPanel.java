    public void populateBillingTable(){
        DefaultTableModel dtm = (DefaultTableModel) billingvaccinejTable1.getModel();
        dtm.setRowCount(0);
        for(InjectionItem injectionItem : billing.getInjectionItemList()){
            Object[] row = new Object[5];
            row[0] = injectionItem.getVaccine().getVaccineID();
            row[1] = injectionItem.getVaccine().getName();
            row[2] = injectionItem.getVaccine().getManufacturerName();
            row[3] = injectionItem.getQuantity();
            row[4] = injectionItem.getVaccine().getPrice();
            dtm.addRow(row);
        }
        
        
    }
    
